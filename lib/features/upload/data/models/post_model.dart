import 'package:dadadu_app/features/upload/domain/entities/post_entity.dart';

class PostModel extends PostEntity {
  const PostModel({
    required super.id,
    required super.userId,
    required super.videoUrl,
    required super.description,
    required super.thumbnailUrl,
    required super.createdAt,
    super.diamonds = 0,
    super.comments = 0,
    super.views = 0,
    super.hashtags,
    super.location,
  });

  // Factory constructor to create PostModel from a Map (e.g., a Supabase query result)
  factory PostModel.fromMap(Map<String, dynamic> map) {
    return PostModel(
      id: map['id'] as String,
      userId: map['user_id'] as String,
      videoUrl: map['video_url'] as String,
      description: map['description'] as String,
      thumbnailUrl: map['thumbnail_url'] as String,
      // Supabase typically returns timestamps as ISO 8601 strings. We parse it into a DateTime object.
      createdAt: DateTime.parse(map['created_at'] as String),
      diamonds: map['diamonds'] as int? ?? 0,
      // Default to 0 if null
      comments: map['comments'] as int? ?? 0, // Default to 0 if null
      views: map['views'] as int? ?? 0,
      // Default to 0 if null
      hashtags: map['hashtags'] as String?,
      // Can be null
      location: map['location'] as String?, // Can be null
    );
  }

  // To Map for saving to Supabase (using snake_case for Supabase column names)
  Map<String, dynamic> toMap() {
    return {
      // 'id' is often auto-generated by the database and not included in inserts.
      'user_id': userId,
      'video_url': videoUrl,
      'description': description,
      'thumbnail_url': thumbnailUrl,
      // Convert DateTime back to an ISO 8601 string for Supabase.
      'created_at': createdAt.toIso8601String(),
      'diamonds': diamonds,
      'comments': comments,
      'views': views,
      'hashtags': hashtags,
      'location': location,
    };
  }

  // Optional: A copyWith method for immutability and easier updates
  PostModel copyWith({
    String? id,
    String? userId,
    String? videoUrl,
    String? description,
    String? thumbnailUrl,
    DateTime? createdAt,
    int? diamonds,
    int? comments,
    int? views,
    String? hashtags,
    String? location,
  }) {
    return PostModel(
      id: id ?? this.id,
      userId: userId ?? this.userId,
      videoUrl: videoUrl ?? this.videoUrl,
      description: description ?? this.description,
      thumbnailUrl: thumbnailUrl ?? this.thumbnailUrl,
      createdAt: createdAt ?? this.createdAt,
      diamonds: diamonds ?? this.diamonds,
      comments: comments ?? this.comments,
      views: views ?? this.views,
      hashtags: hashtags ?? this.hashtags,
      location: location ?? this.location,
    );
  }
}